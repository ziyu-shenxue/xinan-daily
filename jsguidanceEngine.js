/**
 * 章节搜索联想函数（支持关键词+篇章+章节号多维度匹配）
 * @param {string} keyword - 搜索关键词（支持章节号、关键词）
 * @param {string} volume - 篇章筛选（可选：治神篇/治心篇/治身篇/治性篇/治世篇）
 * @returns {Array} 匹配的章节数组
 */
function searchChapterByKeywordAndVolume(keyword, volume = '') {
  if (!window.DAODEJING_81_CHAPTERS || window.DAODEJING_81_CHAPTERS.length === 0) {
    console.warn('⚠️ 章节数据未加载完成，无法进行搜索');
    return [];
  }
  
  const lowerKeyword = keyword.toLowerCase().trim();
  let filteredChapters = window.DAODEJING_81_CHAPTERS;
  
  // 1. 篇章筛选
  if (volume && ['治神篇', '治心篇', '治身篇', '治性篇', '治世篇'].includes(volume)) {
    filteredChapters = filteredChapters.filter(chapter => chapter.volume === volume);
  }
  
  // 2. 关键词/章节号匹配
  if (lowerKeyword) {
    // 支持章节号直接搜索（如输入"3"匹配第3章）
    const chapterNum = parseInt(lowerKeyword);
    if (!isNaN(chapterNum) && chapterNum >= 1 && chapterNum <= 81) {
      const matchedChapter = filteredChapters.find(chapter => chapter.index === chapterNum);
      return matchedChapter ? [matchedChapter] : [];
    }
    
    // 关键词模糊匹配（标题、关键词、篇章、核心目标）
    filteredChapters = filteredChapters.filter(chapter => {
      const matchText = `${chapter.title.toLowerCase()} ${chapter.keywords.join(' ').toLowerCase()} ${chapter.volume.toLowerCase()} ${chapter.coreGoal.toLowerCase()}`;
      return matchText.includes(lowerKeyword);
    });
  }
  
  return filteredChapters;
}

/**
 * 十目践行完成率计算（同步焊缝能量密度）
 */
function updateWeldDensity() {
  const tenItems = ["xinjian_1", "xinjian_2", "xinjian_3", "xinjian_4", "xinjian_5", "xinjian_6", "xinjian_7", "xinjian_8", "xinjian_9", "xinjian_10"];
  let completeCount = 0;
  
  tenItems.forEach(itemId => {
    const radio = document.querySelector(`input[name="${itemId}"][value="践行"]`);
    if (radio && radio.checked) completeCount++;
  });
  
  // 计算密度（1-10分）
  const density = Math.round((completeCount / 10) * 10);
  const densityRange = document.getElementById('weld_density_range');
  const densityValue = document.getElementById('weld_density_value');
  
  if (densityRange && densityValue) {
    densityRange.value = density;
    densityValue.value = density;
    // 更新滑块轨道颜色
    densityRange.style.setProperty('--value', `${density * 10}%`);
    // 同步提示信息
    document.getElementById('density-hint').textContent = `${density}分=十目践行完成率${completeCount * 10}%，实时同步焊缝能量密度`;
  }
}

